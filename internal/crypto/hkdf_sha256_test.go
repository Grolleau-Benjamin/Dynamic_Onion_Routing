package crypto_test

import (
	"bytes"
	"testing"

	"github.com/Grolleau-Benjamin/Dynamic_Onion_Routing/internal/crypto"
)

func TestHKDF_SHA256(t *testing.T) {
	t.Parallel()

	tests := []struct {
		name     string
		secret   []byte
		salt     []byte
		info     []byte
		expected []byte
	}{
		{
			name:     "Basic HKDF derivation with non-empty inputs",
			secret:   []byte("Nicolas_is_great"),
			salt:     []byte("Dynamic_Onion_Routing"),
			info:     []byte("HKDF_SHA256_Test"),
			expected: []byte{0x0a, 0xbf, 0xec, 0xf7, 0xf7, 0x32, 0xa5, 0xa1, 0xdd, 0x96, 0x5b, 0xcb, 0xc0, 0x3e, 0x89, 0x1c, 0xc2, 0x1d, 0x8f, 0x75, 0x5d, 0x96, 0x95, 0x98, 0x91, 0x1c, 0x09, 0xed, 0xa5, 0xc6, 0x80, 0xd8},
		},
		{
			name:     "Empty secret",
			secret:   []byte(""),
			salt:     []byte("Dynamic_Onion_Routing"),
			info:     []byte("HKDF_SHA256_Test"),
			expected: []byte{0x44, 0x72, 0xf0, 0x00, 0xcb, 0x61, 0x73, 0x9f, 0xdd, 0xd5, 0xb9, 0x18, 0x39, 0xa1, 0xdc, 0xcf, 0xec, 0xe5, 0x13, 0xc4, 0x15, 0x9e, 0x1c, 0xba, 0xfd, 0xbc, 0xa4, 0xee, 0x89, 0x76, 0x5e, 0xc1},
		},
		{
			name:     "Empty salt",
			secret:   []byte("Nicolas_is_great"),
			salt:     []byte(""),
			info:     []byte("HKDF_SHA256_Test"),
			expected: []byte{0x45, 0xc2, 0xb8, 0xa6, 0xc1, 0x7e, 0xd2, 0x29, 0xe3, 0x05, 0xe1, 0x29, 0xa8, 0x85, 0x8b, 0xd0, 0x32, 0x43, 0xd3, 0x02, 0x8e, 0xb4, 0x66, 0xf4, 0x5e, 0x6d, 0x84, 0xcd, 0xdc, 0xe8, 0x2e, 0xda},
		},
		{
			name:     "Empty info",
			secret:   []byte("Nicolas_is_great"),
			salt:     []byte("Dynamic_Onion_Routing"),
			info:     []byte(""),
			expected: []byte{0x86, 0x54, 0x8b, 0xf3, 0x38, 0xfd, 0x54, 0x2d, 0x49, 0xe6, 0x16, 0x04, 0xc1, 0x3b, 0x5a, 0x13, 0x46, 0xaf, 0x77, 0xe4, 0xab, 0x3f, 0xa0, 0x54, 0x88, 0x67, 0x19, 0xee, 0x3b, 0xa4, 0x26, 0xd3},
		},
		{
			name:     "nil inputs",
			secret:   nil,
			salt:     nil,
			info:     nil,
			expected: []byte{0xeb, 0x70, 0xf0, 0x1d, 0xed, 0xe9, 0xaf, 0xaf, 0xa4, 0x49, 0xee, 0xe1, 0xb1, 0x28, 0x65, 0x04, 0xe1, 0xf6, 0x23, 0x88, 0xb3, 0xf7, 0xdd, 0x4f, 0x95, 0x66, 0x97, 0xb0, 0xe8, 0x28, 0xfe, 0x18},
		},
	}

	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			t.Parallel()

			got, err := crypto.HKDFSha256(tt.secret, tt.salt, tt.info)
			if err != nil {
				t.Fatalf("HKDFSha256() error = %v", err)
			}

			if !bytes.Equal(got, tt.expected) {
				t.Errorf("HKDFSha256() unexpected result\n\t got: %x\n\t want: %x", got, tt.expected)
			}
		})
	}
}
